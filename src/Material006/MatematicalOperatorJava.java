package Material006;

public class MatematicalOperatorJava {
    public static void main(String[] args) {
        int a = 373, b = 273, c = 10, d = 5, e = 3;
        System.out.println(a + b); // operator +
        System.out.println(a - b); // operator -
        System.out.println(c * d); // operator * (times)
        System.out.println(c / d); // operator / (divided by)
        System.out.println(c % e); // operator % (modulo)
    }
}
