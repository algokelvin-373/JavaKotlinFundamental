package Material007;

public class AugmentedAssignmentJava {
    public static void main(String[] args) {
        int a = 5, b = 6, c = 4;
        a += b; System.out.println(a); // result: 11
        a -= c; System.out.println(a); // result: 7
        a *= c; System.out.println(a); // result: 28
        a /= c; System.out.println(a); // result: 7
    }
}
