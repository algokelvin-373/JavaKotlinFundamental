package CodingFundamental.Material016;

public class IfElseExpressionJava {
    public static void main(String[] args) {
        int a = 6;
        if (a % 2 == 0) {
            System.out.println(a+" is even numbers");
        } else {
            System.out.println(a+" is odd numbers");
        }
    }
}
