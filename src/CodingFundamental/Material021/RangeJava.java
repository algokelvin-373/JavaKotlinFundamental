package CodingFundamental.Material021;

public class RangeJava {
    public static void main(String[] args) {
        //In Java no Range
        for (int a = 1; a <= 10; a++)
            System.out.print(a+" ");
        System.out.println();
        for (int b = 1; b <= 10; b+=2)
            System.out.print(b+" ");
        System.out.println();
        for (int c = 1; c <= 10; c++)
            System.out.print(c+" ");
        System.out.println();
        for (int d = 10; d >= 1; d--)
            System.out.print(d+" ");
        System.out.println();
    }
}
